def create_date_features(data):
    """
    Converts date columns into useful ML features.
    Dates are in day-month-year format, so dayfirst=True is required.
    """
    data = data.copy()

    date_columns = ["order_placed_date", "committed_delivery_date"]

    for col in date_columns:
        data[col] = pd.to_datetime(data[col], errors="coerce", dayfirst=True)

        data[f"{col}_month"] = data[col].dt.month
        data[f"{col}_dayofweek"] = data[col].dt.dayofweek
        data[f"{col}_quarter"] = data[col].dt.quarter

    # Drop original raw date columns after extracting useful date features
    data = data.drop(columns=date_columns)

    return data


